    <section id="presentation">
        <h2>Présentation de la filière</h2>
        <p>Le Bachelor Universitaire de Technologie (BUT) en informatique forme des professionnels capables de concevoir, développer, déployer et maintenir des systèmes informatiques. Il répond aux besoins croissants des entreprises en matière de digitalisation et d'innovation technologique.</p>
        <p>Le BUT informatique propose un enseignement théorique et pratique, permettant aux étudiants d'acquérir des compétences techniques solides dans différents domaines de l'informatique.</p>
    </section>

    <section id="parcoursA">
        <h2>Parcours A : Développement</h2>
        <p>Le parcours A est orienté vers le développement de logiciels et d'applications. Les étudiants y apprendront à :</p>
        <div class="container">
            <div class="item">
                Concevoir et développer des applications web et mobiles.
            </div>
            <div class="item">
                Maîtriser des langages de programmation (comme Java, Python, JavaScript, etc.).
            </div>
            <div class="item">
                Travailler avec des frameworks et des outils de développement modernes.</ca>
            </div>
            <div class="item">
                Gérer des bases de données et optimiser des performances applicatives.
            </div>
        </div>
        <p>Ce parcours est idéal pour ceux qui souhaitent se diriger vers des métiers tels que développeur, ingénieur en logiciel ou architecte de systèmes.</p>
    </section>

    <section id="parcoursB">
        <h2>Parcours B : Systèmes et Réseaux</h2>
        <p>Le parcours B se concentre sur les infrastructures, les réseaux et la sécurité des systèmes informatiques. Les étudiants développeront des compétences dans :</p>
        <div class="container">
            <div class="item">
                L'administration et la gestion des systèmes d'exploitation (Windows, Linux).
            </div>
            <div class="item">
                La mise en place et la gestion des réseaux locaux et étendus.
            </div>
            <div class="item">
                La sécurité des systèmes d'information et la gestion des incidents de sécurité.
            </div>
            <div class="item">
                La virtualisation et le cloud computing.
            </div>
        </div>
        <p>Ce parcours convient à ceux qui s'intéressent aux métiers tels qu'administrateur systèmes et réseaux, ingénieur en cybersécurité ou consultant en systèmes d'information.</p>
    </section>
