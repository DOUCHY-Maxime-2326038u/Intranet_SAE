<h1>Présentation des filières</h1>

<h2>Filière 1 : DUT Informatique</h2>
<p>Le DUT Informatique est une formation qui permet d'acquérir des compétences en développement, en systèmes d'information et en réseaux. Cette formation se déroule sur deux ans et propose un enseignement à la fois théorique et pratique.</p>

<h2>Filière 2 : Licence Professionnelle</h2>
<p>La Licence Professionnelle Informatique permet aux étudiants de se spécialiser après un DUT ou un BTS. Elle propose des parcours spécifiques tels que le développement web, la sécurité des systèmes d'information, ou encore la gestion des bases de données.</p>

<h2>Filière 3 : Autres formations</h2>
<p>Le département propose également d'autres formations liées à l'informatique, comme des formations en alternance, des formations courtes ou encore des certifications professionnelles.</p>

