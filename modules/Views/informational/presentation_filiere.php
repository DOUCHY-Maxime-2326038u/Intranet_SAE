    <h1>Présentation des parcours du BUT Informatique</h1>

<p>Le BUT Informatique est un diplôme national dont l'objectif est de former des informaticiens généralistes. Ces derniers participent à la conception, la réalisation et la mise en œuvre de solutions informatiques. Le département informatique d’Aix-en-Provence propose des parcours permettant aux étudiants d'acquérir des compétences théoriques et pratiques, nécessaires pour une insertion professionnelle rapide ou une poursuite d'études.</p>

<p>Le site d'Aix prépare spécifiquement au métier de développeur fullstack, ainsi qu'à l'administration et à la sécurisation de systèmes informatiques communicants. Les parcours sont offerts dès la deuxième année.</p>



    <div class="parcours">
        <h2>Parcours 1 : Réalisation d’applications - Conception, développement, validation</h2>
        <p>Ce parcours se concentre sur l'ensemble du cycle de vie des logiciels, depuis l'expression du besoin client jusqu'à la conception, la validation, et la maintenance de l'application. Il forme des professionnels capables d'occuper des postes comme concepteur-développeur dans des domaines variés, incluant :</p>
        <ul>
            <li>Développement mobile</li>
            <li>Développement web</li>
            <li>Internet des objets (IoT)</li>
            <li>Développement de jeux vidéo</li>
        </ul>
    </div>

    <div class="parcours">
        <h2>Parcours 2 : Déploiement d’applications communicantes et sécurisées</h2>
        <p>Ce parcours prépare les étudiants à la mise en place et à la sécurisation des systèmes d'information et des applications. Il forme des professionnels pour des métiers comme :</p>
        <ul>
            <li>Administrateur système/réseaux</li>
            <li>Administrateur applicatif</li>
            <li>Spécialiste en cybersécurité</li>
            <li>DevOps</li>
            <li>Intégrateur d'applications</li>
        </ul>
        <p>Les étudiants de ce parcours développent des compétences en gestion et en sécurisation des infrastructures, leur permettant de répondre aux défis actuels de la cybersécurité et de la gestion des systèmes informatiques en réseau.</p>
    </div>
