    <br>
    Ce que propose le BDE :
    <br><br>
    👨 Personnel accueillant :<br>
    Les membres du BDE, principalement composé d'étudiants, sont toujours disponibles pour répondre à vos questions, vous accompagner et vous aider à profiter pleinement de la vie étudiante.
    <br><br>
    🎉 Soirées, événements et sorties à Aix-en-Provence :<br>
    Envie de vous détendre après une semaine intense ? Le BDE organise des soirées festives dans les bars et autres lieux emblématiques de la ville d'Aix-en-Provence. C'est l'occasion idéale pour tisser des liens et découvrir la vie étudiante locale dans une ambiance chaleureuse.
    <br><br>
    🎮 Espace Gaming :<br>
    Amateurs de jeux vidéo, vous allez adorer ! Le BDE dispose d'un espace dédié avec des consoles modernes où vous pourrez vous affronter sur des jeux comme Mario Kart, Super Smash Bros, et bien d'autres. De plus, des tournois de jeux vidéo sont régulièrement organisés pour mettre à l'épreuve vos talents et partager des moments fun avec d'autres étudiants.
    <br><br>
    📚 Un lieu d’entraide pour les révisions :<br>
    Besoin d’un endroit calme pour travailler ou d’un coup de main pour un cours ? Le BDE met à disposition un espace dédié aux révisions où tu peux étudier seul ou en groupe. Que ce soit pour préparer un examen, partager des connaissances entre étudiants de premiere, deuxiemme, ou troisieme année, ou simplement trouver un endroit motivant pour avancer dans tes projets, tu es le bienvenu ! Des sessions d’entraide entre étudiants sont également organisées pour t’aider à mieux comprendre certaines matières.
    <br><br>
    Que ce soit pour se retrouver autour d'une manette, d'un verre ou d'un bon moment entre amis, ou pour améliorer vos revisions, le BDE est là pour rendre votre vie étudiante encore plus riche et mémorable!</p></p>
